const countTreesOnSlope = (grid, deltaX, deltaY) => {
 const rows = grid.length;
 const cols = grid[0].length;
 
 let treeCount = 0; 
 let x = 0;
 let y = 0;
 
 while (y < rows){
  treeCount += grid[y][x] == '#' ? 1 : 0;
  x=(x+deltaX)%cols;
  y+=deltaY;
 }
 return treeCount;
};

const countTrees = input => {
 const grid = input.split('\n').map(x => x.split('')); 
 const p1 = countTreesOnSlope(grid,3,1);
 const p2 = p1 *
   countTreesOnSlope(grid,1,1) *
   countTreesOnSlope(grid,5,1) *
   countTreesOnSlope(grid,7,1) *
   countTreesOnSlope(grid,1,2);
   
  console.log(`Part1: ${p1}`);
  console.log(`Part2: ${p2}`);
}

